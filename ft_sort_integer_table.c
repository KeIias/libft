void	ft_sort_integer_table(int *tab, int size)
{
	int		i;
	int		tmp;
	int		oui;

	oui = 0;
	while (!oui)
	{
		oui = 1;
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = tmp;
				oui = 0;
			}
			i++;
		}
	}
}
