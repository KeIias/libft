#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	i = 0;
	j = (int)ft_strlen(s1);
	while (s2[i] && n > 0)
	{
		s1[j++] = s2[i];
		n--;
		i++;
	}
	s1[j] = '\0';
	return (s1);
}
