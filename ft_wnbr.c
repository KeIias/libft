int		ft_wnbr(const char *s, char c)
{
	int ctr;

	ctr = 0;
	while (*s)
	{
		while (*s == c && *s)
			s++;
		if (*s)
			ctr++;
		while (*s != c && *s)
			s++;
	}
	return (ctr);
}
