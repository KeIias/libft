#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nb;
	int pm;

	i = 0;
	pm = 1;
	nb = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		pm = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (pm * nb);
}
